public class Edge {
	

	//Edge Type Variable
	private boolean edgeType;

	//public constructor for class
	public Edge(bool edgeType){
		this.edgeType = edgeType;
	}

	//get Edge characteristic
	public getEdgeType(){
		return edgeType;
	}

	//set Edge characteristic
	public setEdgeType(){
		return edgeType;
	}

}